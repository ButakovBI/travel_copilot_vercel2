/**
 * Определяет, просит ли пользователь сбросить контекст диалога и начать заново.
 */
const RESET_PATTERNS = [
  /^сбрось\s*(контекст|диалог|вс[ёe])?\.?$/i,
  /^сбросить\s*(контекст|диалог|вс[ёe])?\.?$/i,
  /^забудь\s*(вс[ёe]|всего)?\.?$/i,
  /^начн[eи][тм]\s*заново\.?$/i,
  /^начни\s*заново\.?$/i,
  /^давай\s*заново\.?$/i,
  /^новый\s*диалог\.?$/i,
  /^новый\s*разговор\.?$/i,
  /^начн[eи]\s*с\s*нуля\.?$/i,
  /^заново\s*пожалуйста\.?$/i,
  /^очисти\s*(контекст|историю)?\.?$/i,
  /^очистить\s*(контекст|историю)?\.?$/i,
  /^хочу\s*начать\s*заново\.?$/i,
];

export function isResetContextRequest(message: string): boolean {
  const t = message.trim();
  if (t.length > 80) return false;
  return RESET_PATTERNS.some((re) => re.test(t));
}
